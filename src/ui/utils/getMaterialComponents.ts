export const getMaterialComponents = (speech: string): number[] => {
  const source = speech.split(' ').reverse()[0];

  switch (source) {
    case 'babu':
      return [BabuLegBone, BabuLegFullBurnt, BabuLegHalfBurnt, SoftFabricMediumRoll, SoftFabricMediumStrips];

    case 'dais':
      return [DaisMeatFullBurnt, DaisMeatHalfBurnt, SoftFabricMediumRoll, SoftFabricMediumStrips];

    case 'earth':
      return [CarsiIngot, CopperIngot, EvinonSteelIngot, GoldIngot, IronIngot, OrchiIngot, RedIronIngot, SilverIngot];

    case 'embers':
      return [Coal];

    case 'fungi':
      return [
        MushroomBrownFullBurnt,
        MushroomBrownHalfBurnt,
        MushroomCaveLargeHalfBurnt,
        MushroomCaveSmallFullBurnt,
        MushroomCaveSmallHalfBurnt,
        MushroomGlowingFullBurnt,
        MushroomGlowingHalfBurnt,
        MushroomRedFullBurnt,
        MushroomRedHalfBurnt
      ];

    case 'gems':
      return [CrystalGemBlue, CrystalShardBlue];

    case 'glass':
      return [PotionMedium, SandstoneStone];

    case 'gotera':
      return [GoteraSeedlingOrb, RedwoodGoteraCore];

    case 'produce':
      return [
        AppleCoreBurnt,
        AppleFullBurnt,
        AppleHalfBurnt,
        BlueberryFullBurnt,
        CarrotFullBurnt,
        CarrotHalfBurnt,
        CarrotLeaves,
        EggplantFullBurnt,
        EggplantHalfBurnt,
        GarlicFullBurnt,
        GarlicHalfBurnt,
        GarlicLeaves,
        GarlicRoots,
        OnionFullBurnt,
        OnionHalfBurnt,
        OnionLeaves,
        OnionRoots,
        PotatoFullBurnt,
        PotatoHalfBurnt,
        PotatoSapling,
        PumpkinFullBurnt,
        PumpkinHalfBurnt,
        PumpkinPieceBurnt,
        TomatoFullBurnt,
        TomatoHalfBurnt
      ];

    case 'pyre':
      return [Dynamite, Firework];

    case 'revenant':
      return [PhantomGuard];

    case 'salt':
      return [Salt];

    case 'spriggull':
      return [
        SpriggullDrumstickBone,
        SpriggullDrumstickFullBurnt,
        SpriggullDrumstickHalfBurnt,
        SpriggullFeatherBlue,
        SpriggullFeatherGreen,
        SpriggullFeatherPurple,
        SpriggullFeatherRed,
        SpriggullFletchingBlue,
        SpriggullFletchingRed
      ];

    case 'turabada':
      return [ExplosiveSpike];

    case 'wyrm':
      return [SoftFabricMediumRoll, SoftFabricMediumStrips];

    default:
      return [];
  }
};
